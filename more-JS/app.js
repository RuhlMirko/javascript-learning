// Switch statements

let new_number = Number(prompt("What number do you want to print?"));

console.log(typeof new_number);

switch (new_number) {
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  case 4:
    console.log("thursday");
    break;
}
